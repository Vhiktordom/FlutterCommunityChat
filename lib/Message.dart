class Message {
  final String id;
  final String group;
  final String parent;
  final String content;
  final String firstname;
  final String lastname;
  final String email;
  final String picture;

  Message({
    required this.id,
    required this.group,
    required this.parent,
    required this.content,
    required this.firstname,
    required this.lastname,
    required this.email,
    required this.picture,
  });
}
